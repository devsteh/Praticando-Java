package enquantowhile;

public class Cambalhota {
    public static void main(String[] args) {
        int contCambalhota = 0; //o início do contador será determinado aqui
        while (contCambalhota < 4){
            System.out.println("Cambalhota "+contCambalhota);//imprime o texto junto da quantidade calculada
            contCambalhota = contCambalhota +1;//ou contCambalhota++
        }
    }
}
